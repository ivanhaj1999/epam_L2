package org.collections.arraylist;

public class Dress {
    public int id;
    public String designer;
    public  String size;
    public  int quantity;
    public  String model;
    public Dress(int id, String designer, String size, int quantity, String model){
        this.id=id;
        this.designer=designer;
        this.size=size;
        this.quantity=quantity;
        this.model=model;
    }
}
